
public class BangunDatar {
    public int Segitiga(int alas, int tinggi){
        return (alas*tinggi)/2;
    }
    public int Persegi(int sisi){
        return sisi*sisi;
    }
    public int PersegiPanjang(int panjang, int lebar){
  
        return panjang*lebar;
    }
    public double Lingkaran(int jari){
        return 3.14*jari*jari;
    }
    public double JajarGenjang(int alas, int tinggi){
        return alas*tinggi;
    }

    public double BelahKetupat(int alas, int tinggi){
        return (alas*tinggi)/2;
    }
}
