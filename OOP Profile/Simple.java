
public class Simple {
    
    public int tambah(int a, int b){
        int c = a + b;
        return c;
    }

    public int kurang(int a, int b){
        int c = a - b;
        return c;
    }

    public int kali(int a, int b){
        int c = a * b;
        return c;
    }

    public int bagi(int a, int b){
        int c = a / b;
        return c;
    }
    public int pangkat(int a, int b){
        int c = a ^ b;
        return c;
    }
    public int modulus(int a, int b){
        int c = a % b;
        return c;
    }
}
